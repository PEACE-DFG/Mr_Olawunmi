// NUMBER TO INTEGERS

/*
4 METHODS

1. Math.round(x)-> returns x rounded to its nearest integer

2. Math.ceil(x)-> returns x rounded to its integer

3. Math.floor(x)-> returns x rounded down to its integer

4. Math.trunc(x) -> return integer part of x

5. Math.pow(x, y) -> return the value of x to the power of y

6. Math.sqrt(x)-> returns the square root of x

7. Math.abs(x) -> returns the absolute value of x

8. Math.log(x) -> returns the logarithm of x

9. Math.sin(x) -> returns the sin of x

10. Math.cos(x) -> returns the cos of x

*/

